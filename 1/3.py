output_dir = "splash_outputs"
os.makedirs(output_dir, exist_ok=True)

def render_bitmap(data, width, height, lsb_first=True, invert_color=False):
    img = Image.new("1", (width, height))
    pixels = img.load()
    pages = height // 8
    for page in range(pages):
        for x in range(width):
            byte_index = x + page * width
            if byte_index >= len(data):
                continue
            byte = data[byte_index]
            for bit in range(8):
                y = page * 8 + bit
                if y >= height:
                    continue
                # Выбираем порядок битов
                bit_val = (byte >> bit) & 1 if lsb_first else (byte >> (7 - bit)) & 1
                # Инвертируем цвет, если нужно
                if invert_color:
                    bit_val = 1 - bit_val
                pixels[x, y] = 0 if bit_val else 255
    return img

